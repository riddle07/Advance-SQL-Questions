CREATE TABLE P17(PRODUCT VARCHAR(255), QUANTITY INT)
INSERT INTO
	P17
VALUES
('PENCIL', 3),
	('ERASER', 4),
	('NOTEBOOK', 2);

WITH CTE AS (
	SELECT
		PRODUCT,
		1 AS Q,
		QUANTITY
	FROM
		P17
	UNION
	ALL
	SELECT
		c.PRODUCT,
		c.Q,
		c.QUANTITY -1
	FROM
		CTE c
	WHERE
		c.QUANTITY > 1
)
SELECT
	PRODUCT,
	Q
FROM
	CTE