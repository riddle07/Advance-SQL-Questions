SELECT
	[EMPLOYEE ID]
FROM
	(
		SELECT
			[EMPLOYEE ID],
			SUM(
				(UNICODE(SUBSTRING(LICENSE, 7, 1)) -65) *(UNICODE(SUBSTRING(LICENSE, 7, 1)) -65)
			) AS HASHVALUE
		FROM
			P9
		GROUP BY
			[EMPLOYEE ID]
	) T
WHERE
	T.HASHVALUE IN (
		SELECT
			SUM(
				(UNICODE(SUBSTRING(LICENSE, 7, 1)) -65) *(UNICODE(SUBSTRING(LICENSE, 7, 1)) -65)
			) AS HASHVALUE
		FROM
			P9 K
		WHERE
			T.[EMPLOYEE ID] <> K.[EMPLOYEE ID]
		GROUP BY
			[EMPLOYEE ID]
	)