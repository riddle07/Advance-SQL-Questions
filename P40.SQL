('BALTIMORE'),
	('CHICAGO'),
	('DENVER');

;

WITH CTE AS(
	SELECT
		*,
		ROW_NUMBER() OVER(
			ORDER BY
				CITY
		) AS RW
	FROM
		P40
),
CTE1 AS(
	SELECT
		*,
		CASE
			WHEN RW % 2 = 0 THEN 0
			ELSE 1
		END AS CHECK1
	FROM
		CTE
)
SELECT
	CITY
FROM
	CTE1
ORDER BY
	CHECK1 ASC,
	RW ASC