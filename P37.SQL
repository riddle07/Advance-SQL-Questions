CREATE TABLE P37(
	[ORDER ID] INT,
	DISTRIBUTOR VARCHAR(255),
	FACILITY VARCHAR(255),
	[ZONE] VARCHAR(255),
	AMOUNT INT
)
INSERT INTO
	P37
VALUES
(1, 'ACME', '123', 'ABC', 100),
	(2, 'ACME', '123', 'ABC', 75),
	(3, 'DIRECT PARTS', '789', 'XYZ', 150),
	(4, 'DIRECT PARTS', '789', 'XYZ', 125)
SELECT
	RANK() OVER(
		ORDER BY
			DISTRIBUTOR,
			FACILITY,
			[ZONE],
			AMOUNT
	) [CRITERIA ID],
	[ORDER ID],
	DISTRIBUTOR,
	FACILITY,
	[ZONE],
	AMOUNT
FROM
	P37;