CREATE TABLE P43(
	[ORDER] INT,
	[CUSTOMER ID] INT,
	QUANTITY INT
);

INSERT INTO
	P43
VALUES
(1, 1001, 5),
	(2, 1001, 8),
	(3, 1001, 3),
	(4, 1001, 7),
	(1, 2002, 4),
	(2, 2002, 9);

SELECT
	*,
	MIN(QUANTITY) OVER(
		PARTITION BY [CUSTOMER ID]
		ORDER BY
			[ORDER]
	) [MIN VALUE]
FROM
	P43;