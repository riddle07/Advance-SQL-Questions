SELECT
	P33A.[ORDER ID],
	P33A.PRODUCT
FROM
	P33A
	JOIN(
		SELECT
			PRODUCT,
			SUM([DAYS TO MANUFACTURE]) [SUM]
		FROM
			P33B
		GROUP BY
			PRODUCT
	) P33B ON P33A.PRODUCT = P33B.PRODUCT
WHERE
	P33A.[DAYS TO DELIVER] >= P33B.SUM
