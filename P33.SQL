	[ORDER ID] INT,
	PRODUCT VARCHAR(255),
	[DAYS TO DELIVER] INT
);

CREATE TABLE P33B(
	PART VARCHAR(255),
	PRODUCT VARCHAR(255),
	[DAYS TO MANUFACTURE] INT
)
