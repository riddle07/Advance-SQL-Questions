CREATE TABLE P1A(ITEM VARCHAR(250));

CREATE TABLE P1B(ITEM VARCHAR(250));

INSERT INTO
	P1A
VALUES
('SUGAR'),
('BREAD'),
	('RICE'),
	('SODA'),
	('FLOUR');

INSERT INTO
	P1B
VALUES
('SUGAR'),
	('BREAD'),
	('BUTTER'),
	('CHEESE'),
	('FRUIT');

SELECT
	ISNULL(P1A.ITEM, ' ') [ITEM CART 1],
	ISNULL(P1B.ITEM, ' ') [ITEM CART 2]
FROM
	P1A FULL
	OUTER JOIN P1B ON P1A.ITEM = P1B.ITEM;