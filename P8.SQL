CREATE TABLE P8(
	WORKFLOW VARCHAR(255),
	[CASE 1] INT,
	[CASE 2] INT,
	[CASE 3] INT
);

INSERT INTO
	P8
VALUES
	('ALPHA', 0, 0, 0),
	('BRAVO', 0, 1, 1),
	('CHARLIE', 1, 0, 0),
	('DELTA', 0, 0, 0);

SELECT
	WORKFLOW,
	SUM(PASSED) PASSED
FROM
	P8 UNPIVOT (
		PASSED FOR [CASE] IN ([CASE 1], [CASE 2], [CASE 3])
	) AS UNPIVOTTTED
GROUP BY
	WORKFLOW