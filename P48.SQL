CREATE TABLE P48([CUSTOMER ID] INT, [YEAR] INT);

INSERT INTO
	P48
VALUES
(1001, 2018),
	(1001, 2019),
	(1001, 2020),
	(2002, 2020),
	(2002, 2021),
	(3003, 2018),
	(3003, 2021),
	(4004, 2019),
	(4004, 2020),
	(4004, 2021);

SELECT
	[CUSTOMER ID]
FROM
	P48 A
WHERE
	(YEAR -1) IN (
		SELECT
			YEAR
		FROM
			P48 B
		WHERE
			A.[CUSTOMER ID] = B.[CUSTOMER ID]
	)
	AND (YEAR -2) IN (
		SELECT
			YEAR
		FROM
			P48 B
		WHERE
			A.[CUSTOMER ID] = B.[CUSTOMER ID]
	)