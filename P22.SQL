SELECT
	WORKFLOW,
	[MESSAGE]
FROM
	(
		SELECT
			WORKFLOW,
			[MESSAGE],
			[OCCURRENCES],
			RANK() OVER(
				PARTITION BY [MESSAGE]
				ORDER BY
					[OCCURRENCES] DESC
			) RNK
		FROM
			P22
	) T
WHERE
	T.RNK = 1