SELECT
	[CANDIDATE ID]
FROM
	(
		SELECT
			P7A.[CANDIDATE ID],
			COUNT(*) [COUNT]
		FROM
			P7A
			LEFT JOIN P7B ON P7A.DESCRIPTION = P7B.DESCRIPTION
		WHERE
			P7B.DESCRIPTION IS NOT NULL
		GROUP BY
			P7A.[CANDIDATE ID]
	) T
WHERE
	T.[COUNT] = (
		SELECT
			COUNT(*)
		FROM
			P7B
	);